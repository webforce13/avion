                    <h2 class="intro-text text-center"><strong>Le recyclage 
                        et pourquoi</strong>
                    </h2>
                    <hr>
                    <img class="img-responsive img-border img-left" src="../public/assets/images/28.png" width="150px" height="150px" alt="icone">
                    <hr class="visible-xs">
                    <p>Le recyclage est un procédé de traitement des métaux, plastiques, déchets (déchet industriel ou ordures ménagères) qui permet de réintroduire, dans le cycle de production d'un produit, des matériaux qui composaient un produit similaire arrivé en fin de vie, ou des résidus de fabrication.</p>
                    <p>L'avion a, comme d'autres moyens de transport motorisés, un impact sur l'environnement, notamment en contribuant au dérèglement climatique. C'est au décollage, quand les réacteurs fonctionnent à pleine puissance qu'il consomme et pollue le plus (CO2, NOx, Métaux lourds contenus dans le kérosène ou provenant de l'usure des tuyères, imbrûlés…). Les avions sont aussi une source de nuisance sonore très importante aux abords des aéroports et dans les zones d'entraînement d'avions militaires. Les populations les plus nombreuses à être impactées par les nuisances sonores des avions sont celles habitant dans l'axe de l'aéroport, notamment à Roissy ou Toulouse en France, à Bruxelles en Belgique. A Francfort en Allemagne, les riverains ont obtenu par voie judiciaire la suppression des vols de nuit en raison des nuisances occasionnées[réf. nécessaire]. Les aérosols et la vapeur d'eau émise par les tuyères contribuent aussi à la formation de nuages artificiels (traînées de condensation) qui modifient le système atmosphérique et climatique[réf. nécessaire], avec un effet de refroidissement à court terme, mais de réchauffement à long terme.</p>
                    <p>Pour l'aviation civile, les émissions de CO2 par passager et par vol sont supérieures à celle du transport ferroviaire (30 fois plus environ par passager). Même rempli, et sur de longues distances, un avion émet, en moyenne, plus de gaz à effet de serre par passager qu'une automobile. Par exemple, le long courrier Boeing 777 consomme environ 2,4 à 2,6 L/100 km par passager contre 1 à 1,6 L/100 km par passager pour une automobile avec cinq personnes à bord9[non neutre]. Un calculateur simplifié de CO2 est disponible sur le site de la DGAC10.</p>
                    <p>Les avions émettent aussi d'importantes quantités de NOx (oxydes d'azote, polluant et également contributeur au réchauffement climatique). Actuellement, ces NOx ne peuvent être traités par des pots catalytiques comme dans les cas des automobiles modernes.</p>
                    <p>Globalement, on évalue aujourd'hui à 3 % de la libération totale de gaz à effet de serre la part due à l'aviation, mais c'est le secteur, qui avec la marine marchande augmente le plus rapidement. Dans l'Union européenne, des mesures sont prises pour stopper cette augmentation : le protocole de Kyoto sera mis en application à partir de 2012, par l'inclusion de l'aviation civile dans le Système d'échange de quotas d'émissions européen, qui a pour objectif de réduire les émissions de CO2 de 3 % en 2012, et de 5 % durant la période 2013-2020, comparées aux émissions moyennes constatées dans la période 2004-2006.

                    </p>
