                    <h2 class="intro-text text-center"><strong>Le démentélement</strong>
                    </h2>

                    <hr>
                    <p>Si le terme de « démolition » a souvent une connotation négative (par l'image de destruction totale, ou par l'analogie entre un chantier de démolition et un « cimetière d'avions » parfois trouvée dans la presse), le processus de démolition permet une réutilisation des matériaux qu'il contient, et donc un avantage économique et écologique. Les équipements sont en général revendus, les matériaux refondus (dans le cas de l'acier) ou recyclés (pour le bronze et le cuivre).
                    </p>
                    <p>Le départ d'un avion pour la casse peut être émotionnellement chargé, par exemple dans le cas d'un paquebot célèbre ou d'un avion de guerre ayant remporté certaines batailles. De manière générale, il est courant que l'équipage soit attaché à « son » avion.</p>
